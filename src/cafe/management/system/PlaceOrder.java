    public void validateField() {
        String customerName = txt_nameC.getText();
        String customerMobileNumber = txt_mobileNumberC.getText();
        String customerEmail = txt_emailC.getText();
        if (!customerEmail.equals("") && customerMobileNumber.matches(mobileNumberPattern) && customerMobileNumber.length() == 12 && customerEmail.matches(emailPattern) && grandTotal > 0) {
            btn_generate.setEnabled(true);
        } else {
            btn_generate.setEnabled(false);
        }
    }

