    private void btn_generateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_generateActionPerformed
        String customerName = txt_nameC.getText();
        String customerMobileNumber = txt_mobileNumberC.getText();
        String customerEmail = txt_emailC.getText();
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
        SimpleDateFormat dayFormat = new SimpleDateFormat("EEEE", new Locale("id", "ID"));
        Date date = new Date();
        String todayDate = dFormat.format(date);
        String todayDay = dayFormat.format(date); // Get the day name
        String total = String.valueOf(grandTotal);
        String createdBy = userEmail;
        String status = "unpaid"; // Status default "unpaid"

        // Create a new Bill object
        Bill bill = new Bill();
        bill.setId(billId);
        bill.setName(customerName);
        bill.setMobileNumber(customerMobileNumber);
        bill.setEmail(customerEmail);
        bill.setDate(todayDate);
        bill.setTotal(total);
        bill.setCreatedBy(createdBy);
        bill.setStatus(status);

        // Save the bill using BillDao (assuming BillDao is correctly implemented)
        BillDao.save(bill);

        // Create the PDF document
        String path = "E:\\";
        com.itextpdf.text.Document doc = new com.itextpdf.text.Document();

        try {
            PdfWriter.getInstance(doc, new FileOutputStream(path + billId + ".pdf"));
            doc.open();

            // Adding content to the PDF
            Paragraph cafeName = new Paragraph("Cafe Management System\n", new Font(Font.FontFamily.TIMES_ROMAN, 18, Font.BOLD));
            cafeName.setAlignment(Element.ALIGN_CENTER);
            doc.add(cafeName);

            Paragraph starLine = new Paragraph("*******************************************************************************************************");
            doc.add(starLine);

            Paragraph billInfo = new Paragraph("Bill ID: " + billId + "\nCustomer Name: " + customerName + "\nTotal Paid: " + grandTotal + "\n");
            doc.add(billInfo);
            doc.add(starLine);

            // Add paragraph for order details
            Paragraph paymentDetailsHeader = new Paragraph("Detail Pemesanan", new Font(Font.FontFamily.TIMES_ROMAN, 16, Font.BOLD));
            paymentDetailsHeader.setAlignment(Element.ALIGN_CENTER);
            doc.add(paymentDetailsHeader);

            // Add vertical space between paragraphs
            doc.add(new Paragraph("\n"));

            // Adding table for order details
            PdfPTable tbl = new PdfPTable(4);
            tbl.setWidthPercentage(100); // Set table width to 100% of page width
            tbl.addCell("Nama");
            tbl.addCell("Harga");
            tbl.addCell("Kuantitas");
            tbl.addCell("Total");

            for (int i = 0; i < jTable2.getRowCount(); i++) {
                String n = jTable2.getValueAt(i, 0).toString();
                String d = jTable2.getValueAt(i, 1).toString();
                String r = jTable2.getValueAt(i, 2).toString();
                String q = jTable2.getValueAt(i, 3).toString();
                tbl.addCell(n);
                tbl.addCell(d);
                tbl.addCell(r);
                tbl.addCell(q);
            }

            doc.add(tbl);
            doc.add(starLine);

            // Adding thank you message
            Paragraph thanksMsg = new Paragraph("Terima kasih atas kunjungan Anda.", new Font(Font.FontFamily.TIMES_ROMAN, 14, Font.ITALIC));
            thanksMsg.setAlignment(Element.ALIGN_CENTER);
            doc.add(thanksMsg);

            // Add date and signature at the bottom right
            Paragraph dateAndSignature = new Paragraph("Jakarta, " + todayDay + " " + todayDate + "\n\n(Akbar Coffee Shop)", new Font(Font.FontFamily.TIMES_ROMAN, 12, Font.NORMAL));
            dateAndSignature.setAlignment(Element.ALIGN_RIGHT);
            doc.add(dateAndSignature);

            // Open the generated PDF file
            OpenPdf.openById(String.valueOf(billId));
        } catch (DocumentException | FileNotFoundException e) {
            JOptionPane.showMessageDialog(null, "Error creating PDF: " + e.getMessage());
        } finally {
            if (doc != null) {
                doc.close();
            }
        }

        setVisible(false);
        new PlaceOrder(createdBy).setVisible(true);

    }//GEN-LAST:event_btn_generateActionPerformed

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        int index = jTable2.getSelectedRow();
        int a = JOptionPane.showConfirmDialog(null, "Apakah anda ingin menghapus product ini?", "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            TableModel model = jTable2.getModel();
            String total = model.getValueAt(index, 3).toString();
            grandTotal = grandTotal - Integer.parseInt(total);
            label_grand.setText(String.valueOf(grandTotal));
            ((DefaultTableModel) jTable2.getModel()).removeRow(index);
        }
    }//GEN-LAST:event_jTable2MouseClicked

