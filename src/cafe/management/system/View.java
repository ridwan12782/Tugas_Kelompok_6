    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        // Membuat objek Product dan mengatur nilai-nilai berdasarkan input pengguna
        Product product = new Product();
        int id = Integer.parseInt(label_id.getText());
        product.setId(id);
        product.setName(txt_name.getText().trim());
        product.setCategory((String) cmb_category.getSelectedItem());
        product.setPrice(txt_price.getText().trim());

        try {
            // Memastikan harga valid (lebih besar dari nol)
            double price = Double.parseDouble(txt_price.getText().trim());
            if (price <= 0) {
                JOptionPane.showMessageDialog(null, "Harga produk harus lebih besar dari nol", "Kesalahan", JOptionPane.ERROR_MESSAGE);
                return; // Menghentikan proses update jika harga tidak valid
            }

            // Memanggil metode update dari ProductDao
            ProductDao.update(product);

            // Menyembunyikan frame setelah update
            setVisible(false);

            // Menampilkan view yang sesuai setelah update
            new View().setVisible(true);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Format harga tidak valid", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Kesalahan: " + e.getMessage(), "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btn_updateActionPerformed

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        ArrayList<Product> list = ProductDao.getAllRecords();
        Iterator<Product> itr = list.iterator();
        while (itr.hasNext()) {
            Product productObj = itr.next();
            dtm.addRow(new Object[]{productObj.getId(), productObj.getName(), productObj.getCategory(), productObj.getPrice()});

        }

    }//GEN-LAST:event_formComponentShown

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String id = model.getValueAt(index, 0).toString();
        label_id.setText(id);
        String name = model.getValueAt(index, 1).toString();
        txt_name.setText(name);
        String category = model.getValueAt(index, 2).toString();
        String price = model.getValueAt(index, 3).toString();
        txt_price.setText(price);

        btn_update.setEnabled(true);
        btn_delete.setEnabled(true);
        cmb_category.removeAllItems();
        cmb_category.addItem(category);

        ArrayList<Category> categoryList = CategoryDao.getAllRecord();
        Iterator<Category> categoryItr = categoryList.iterator();
        while (categoryItr.hasNext()) {
            Category categoryObj = categoryItr.next();
            if (!categoryObj.getName().equals(category)) {
                cmb_category.addItem(categoryObj.getName());
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

