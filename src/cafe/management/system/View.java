    private void btn_cetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cetakActionPerformed
        // TODO add your handling code here:                                             
    // Mengumpulkan detail produk
    String productName = txt_name.getText();
    String productCategory = (String) cmb_category.getSelectedItem();
    String productPrice = txt_price.getText();

    SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
    SimpleDateFormat dayFormat = new SimpleDateFormat("EEEE", new Locale("id", "ID"));
    Date date = new Date();
    String todayDate = dFormat.format(date);
    String todayDay = dayFormat.format(date);
    String createdBy = "admin"; // Gantilah dengan informasi pengguna yang sebenarnya jika tersedia
    String path = "E:\\";
    com.itextpdf.text.Document doc = new com.itextpdf.text.Document();
    
    try {
        PdfWriter.getInstance(doc, new FileOutputStream(path + label_id.getText() + ".pdf"));
        doc.open();
        
        // Menambahkan konten ke PDF
        Paragraph cafeName = new Paragraph("Cafe Management System\n", new Font(Font.FontFamily.TIMES_ROMAN, 18, Font.BOLD));
        cafeName.setAlignment(Element.ALIGN_CENTER);
        doc.add(cafeName);
        
        Paragraph starLine = new Paragraph("*******************************************************************************************************");
        doc.add(starLine);
        
        Paragraph billInfo = new Paragraph("Menu Tersedia\n", new Font(Font.FontFamily.TIMES_ROMAN, 18, Font.BOLD));
        billInfo.setAlignment(Element.ALIGN_CENTER);
        doc.add(billInfo);
        doc.add(starLine);

        PdfPTable tbl = new PdfPTable(3);
        tbl.addCell("Nama");
        tbl.addCell("Kategori");
        tbl.addCell("Harga");
        
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            String n = jTable1.getValueAt(i, 1).toString();
            String c = jTable1.getValueAt(i, 2).toString();
            String p = jTable1.getValueAt(i, 3).toString();
            tbl.addCell(n);
            tbl.addCell(c);
            tbl.addCell(p);
        }

        doc.add(tbl);
        doc.add(starLine);

        Paragraph thanksMsg = new Paragraph("Terimakasih, Atas kunjungan Anda.", new Font(Font.FontFamily.TIMES_ROMAN, 14, Font.ITALIC));
        thanksMsg.setAlignment(Element.ALIGN_CENTER);
        doc.add(thanksMsg);

        // Add date and signature at the bottom right
        Paragraph dateAndSignature = new Paragraph("Jakarta, " + todayDay + ", " + todayDate + "\n\n(Akbar Coffee Shop)", new Font(Font.FontFamily.TIMES_ROMAN, 12, Font.NORMAL));
        dateAndSignature.setAlignment(Element.ALIGN_RIGHT);
        doc.add(dateAndSignature);

        // Membuka file PDF yang dihasilkan
        OpenPdf.openById(label_id.getText());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    doc.close();

    }//GEN-LAST:event_btn_cetakActionPerformed

