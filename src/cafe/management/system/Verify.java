    public void getAllRecords(String email) {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<user> list = UserDao.getAllRecords(email);
        Iterator<user> itr = list.iterator();
        while (itr.hasNext()) {
            user userObj = itr.next();
            if (!userObj.getEmail().equals("ridwanspeed8@gmail.com")) {
                dtm.addRow(new Object[]{
                    userObj.getId(),
                    userObj.getName(),
                    userObj.getEmail(),
                    userObj.getMobileNumber(),
                    userObj.getAddress(),
                    userObj.getSecurityQuestion(),
                    userObj.getStatus()
                });
            }
        }
    }

