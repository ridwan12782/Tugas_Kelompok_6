    private void btn_cetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cetakActionPerformed
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
        SimpleDateFormat dayFormat = new SimpleDateFormat("EEEE", new Locale("id", "ID"));
    Date date = new Date();
    String todayDate = dFormat.format(date);
    String todayDay = dayFormat.format(date);
    String createdBy = "admin"; // Replace with actual user info if available
    String path = "E:\\"; // Change the path as required
    Document doc = new Document();

    try {
        PdfWriter.getInstance(doc, new FileOutputStream(path + "Verification_" + todayDate + ".pdf"));
        doc.open();

        // Add content to PDF
        Paragraph cafeName = new Paragraph("Cafe Management System\n", new Font(Font.FontFamily.TIMES_ROMAN, 18, Font.BOLD));
        cafeName.setAlignment(Element.ALIGN_CENTER);
        doc.add(cafeName);

        Paragraph starLine = new Paragraph("*******************************************************************************************************");
        doc.add(starLine);

        Paragraph verificationInfo = new Paragraph("Detail Customer\n", new Font(Font.FontFamily.TIMES_ROMAN, 16, Font.BOLD));
        verificationInfo.setAlignment(Element.ALIGN_CENTER);
        doc.add(verificationInfo);
        doc.add(starLine);

        PdfPTable tbl = new PdfPTable(7);
        tbl.addCell("ID");
        tbl.addCell("Name");
        tbl.addCell("Email");
        tbl.addCell("Mobile Number");
        tbl.addCell("Address");
        tbl.addCell("Security Question");
        tbl.addCell("Status");

        for (int i = 0; i < jTable1.getRowCount(); i++) {
            String id = jTable1.getValueAt(i, 0).toString();
            String name = jTable1.getValueAt(i, 1).toString();
            String userEmail = jTable1.getValueAt(i, 2).toString();
            String mobile = jTable1.getValueAt(i, 3).toString();
            String address = jTable1.getValueAt(i, 4).toString();
            String securityQuestion = jTable1.getValueAt(i, 5).toString();
            String status = jTable1.getValueAt(i, 6).toString();
            tbl.addCell(id);
            tbl.addCell(name);
            tbl.addCell(userEmail);
            tbl.addCell(mobile);
            tbl.addCell(address);
            tbl.addCell(securityQuestion);
            tbl.addCell(status);
        }

        doc.add(tbl);
        doc.add(starLine);

        Paragraph thanksMsg = new Paragraph("Terimakasih, atas kunjungan Anda.", new Font(Font.FontFamily.TIMES_ROMAN, 14, Font.ITALIC));
        thanksMsg.setAlignment(Element.ALIGN_CENTER);
        doc.add(thanksMsg);

        // Add date and signature at the bottom right
        Paragraph dateAndSignature = new Paragraph("Jakarta, " + todayDay + ", " + todayDate + "\n\n(Akbar Coffee Shop)", new Font(Font.FontFamily.TIMES_ROMAN, 12, Font.NORMAL));
        dateAndSignature.setAlignment(Element.ALIGN_RIGHT);
        doc.add(dateAndSignature);

        // Close the document before opening PDF
        doc.close();

        // Open the generated PDF after it's closed
        OpenPdf.openById("Verification_" + todayDate);
        JOptionPane.showMessageDialog(null, "Detail Customers successfully generated.", "Success", JOptionPane.INFORMATION_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_btn_cetakActionPerformed

