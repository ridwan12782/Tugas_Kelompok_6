    public void tableDetails() {
        String date = jTextField1.getText();
        String incDec = (String) jComboBox1.getSelectedItem();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0); // Menghapus semua baris yang ada dari tabel

        try {
            if (incDec.equals("INC")) {
                ArrayList<Bill> list = BillDao.getAllRecordsByInc(date);
                for (Bill billObj : list) {
                    // Menambahkan baris dengan semua informasi termasuk status
                    dtm.addRow(new Object[]{billObj.getId(), billObj.getName(), billObj.getMobileNumber(), billObj.getEmail(), billObj.getDate(), billObj.getTotal(), billObj.getCreatedBy(), billObj.getStatus()});
                }
            } else {
                ArrayList<Bill> list = BillDao.getAllRecordsByDesc(date);
                for (Bill billObj : list) {
                    // Menambahkan baris dengan semua informasi termasuk status
                    dtm.addRow(new Object[]{billObj.getId(), billObj.getName(), billObj.getMobileNumber(), billObj.getEmail(), billObj.getDate(), billObj.getTotal(), billObj.getCreatedBy(), billObj.getStatus()});
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

