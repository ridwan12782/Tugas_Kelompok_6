    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();

        if (index >= 0) {
            String idStr = model.getValueAt(index, 0).toString();

            if (!idStr.isEmpty() && idStr.matches("\\d+")) {
                try {
                    int id = Integer.parseInt(idStr);

                    double amountPaid = BillDao.calculatePaymentAmount(String.valueOf(id));
                    String paymentDateStr = new SimpleDateFormat("yyyy-MM-dd").format(new Date());
                    Date paymentDate = null;

                    try {
                        paymentDate = new SimpleDateFormat("yyyy-MM-dd").parse(paymentDateStr);
                    } catch (ParseException e) {
                        JOptionPane.showMessageDialog(null, e.getMessage(), "Date Parsing Error", JOptionPane.ERROR_MESSAGE);
                    }

                    Bill bill = BillDao.getBillById(String.valueOf(id));
                    if (bill != null) {
                        String customerName = bill.getName();
                        String customerEmail = bill.getEmail();
                        String customerMobile = bill.getMobileNumber();

                        PaymentHistory payment = new PaymentHistory(0, id, amountPaid, paymentDate, "Paid", customerName, customerEmail, customerMobile);

                        PaymentHistoryDao.save(payment);

                        BillDao.updatePaymentStatus(String.valueOf(id), "Paid");

                        tableDetails();

                        JOptionPane.showMessageDialog(null, "Pembayaran dengan ID " + id + " berhasil dilakukan. Riwayat pembayaran telah disimpan.");
                    } else {
                        JOptionPane.showMessageDialog(null, "Tagihan dengan ID " + id + " tidak ditemukan.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "ID harus berupa angka.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "ID tidak boleh kosong dan harus berupa angka.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Pilih baris yang valid dari tabel.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jTable1MouseClicked

